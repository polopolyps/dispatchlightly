package com.polopoly.ps.dispatchlightly.exception;

public class CannotResolveTemplateException extends RenderException {

	public CannotResolveTemplateException() {
		super();
	}

	public CannotResolveTemplateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CannotResolveTemplateException(String arg0) {
		super(arg0);
	}

	public CannotResolveTemplateException(Throwable arg0) {
		super(arg0);
	}

}
