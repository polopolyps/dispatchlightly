package com.polopoly.ps.dispatchlightly.exception;

public class ModelFactoryException extends Exception {

	public ModelFactoryException() {
		super();
	}

	public ModelFactoryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ModelFactoryException(String arg0) {
		super(arg0);
	}

	public ModelFactoryException(Throwable arg0) {
		super(arg0);
	}

}
